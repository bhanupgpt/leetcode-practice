class Solution {
  public:
    int kthElement(vector<int>& arr, vector<int>& brr, int k) {
        // code here
        vector<int> temp(arr.begin(), arr.end());
        temp.insert(temp.end(), brr.begin(), brr.end());
        sort(temp.begin(), temp.end());
        return temp[k-1];
    }
};

